package org.hj.model;

public class Product_DetailVO {
    private int noDetail;
    private String nameDetail;
    private int priceDetail;

    // Getters and Setters
    public int getNoDetail() {
        return noDetail;
    }

    public void setNoDetail(int noDetail) {
        this.noDetail = noDetail;
    }

    public String getNameDetail() {
        return nameDetail;
    }

    public void setNameDetail(String nameDetail) {
        this.nameDetail = nameDetail;
    }

    public int getPriceDetail() {
        return priceDetail;
    }

    public void setPriceDetail(int priceDetail) {
        this.priceDetail = priceDetail;
    }
}
